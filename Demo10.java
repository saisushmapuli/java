
public class Demo10 {
	public static void main(String []args) {
		String name="SAI SUSHMA PULI";
		System.out.println(name);
		System.out.println("====================");
		System.out.println(name.toUpperCase());
		System.out.println("====================");
		System.out.println(name.toLowerCase());
		System.out.println("====================");
		System.out.println(name.charAt(2));
		System.out.println("====================");
		System.out.println(name.substring(2));
		System.out.println("====================");
		System.out.println(name.contains("SAI"));
		System.out.println("====================");
		System.out.println(name.contains("Aam"));
		System.out.println("====================");
		System.out.println(name.substring(2,9));
		System.out.println("====================");
	}
}
		
		